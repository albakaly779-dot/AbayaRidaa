export function findProductByCode(code: string): ProductModel | undefined {
  const normalized = code.toUpperCase().replace(/\s+/g, "-").trim();
  return PRODUCT_CATALOG.find(
    (p) => p.code === normalized || p.code.replace(/-/g, "").includes(normalized.replace(/-/g, ""))
  );
}

export function searchProducts(query: string): ProductModel[] {
  const q = query.toLowerCase().trim();
  if (!q) return PRODUCT_CATALOG;
  return PRODUCT_CATALOG.filter(
    (p) =>
      p.code.toLowerCase().includes(q) ||
      p.name.includes(query) ||
      p.category.includes(query)
  );
}

export function getCategories(): string[] {
  return [...new Set(PRODUCT_CATALOG.map((p) => p.category))];
}

export function calculateCost(fabricMeters: number = 2.25, fabricPricePerMeter: number = 1000, tarhaCost: number = 300, extrasCost: number = 200) {
  const fabricCost = fabricMeters * fabricPricePerMeter;
  return { fabricCost, tarhaCost, extrasCost, totalCost: fabricCost + tarhaCost + extrasCost };
}
