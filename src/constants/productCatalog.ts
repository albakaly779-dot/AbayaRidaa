export interface ProductModel {
  code: string;
  name: string;
  category: string;
  buyPrice: number;
  sellPrice: number;
  fabricMeters: number;
  fabricPricePerMeter: number;
  tarhaCost: number;
  extrasCost: number;
  totalCost: number;
  size?: string;
  color?: string;
  description?: string;
}

function makeProduct(
  code: string,
  name: string,
  category: string,
  sellPrice: number,
  color: string = "أسود",
  fabricMeters: number = 2.25,
  fabricPricePerMeter: number = 1000,
  tarhaCost: number = 300,
  extrasCost: number = 200
): ProductModel {
  const fabricCost = fabricMeters * fabricPricePerMeter;
  const totalCost = fabricCost + tarhaCost + extrasCost;
  return {
    code, name, category, sellPrice,
    buyPrice: totalCost,
    fabricMeters, fabricPricePerMeter, tarhaCost, extrasCost, totalCost,
    color,
  };
}

// Shilat/accessories use different cost structure
function makeAccessory(
  code: string, name: string, category: string,
  buyPrice: number, sellPrice: number, color: string = "أسود"
): ProductModel {
  return {
    code, name, category, buyPrice, sellPrice,
    fabricMeters: 0, fabricPricePerMeter: 0, tarhaCost: 0, extrasCost: 0,
    totalCost: buyPrice, color,
  };
}

